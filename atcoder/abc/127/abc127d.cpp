#include <bits/stdc++.h>
using namespace std;
typedef long long int lli;

void merge(lli arr[], int l, int m, int r); 
void mergeSort(lli arr[], int l, int r);

void mergePair(lli arr[], int l, int m, int r); 
void mergeSortPair(lli arr[], int l, int r);

int main(){
  lli n, m;
  cin >> n >> m;

  lli a[n];
  pair<lli,lli> bc[m];
  lli sum = 0;
  lli count = 0;
  
  for(lli i = 0; i < n; i++)
    cin >> a[i];

  for(lli i = 0; i < m; i++)
    cin >> bc[i].first >> bc[i].second;

  mergeSort(a, 0, n-1);
  mergeSortPair(bc, 0, m-1);
  

  for(int i = 0; i < n; i++){
    if(a[i] > bc[count].first)
      break;
    if(bc[count].second == 0)
      count++;
    if(count >= m)
      break;

    a[i] = bc[count].first;
    bc[count].second -= 1;
  }

  for(int i = 0; i < n; i++)
    sum += a[i];

  cout << sum << endl;
}


void merge(lli arr[], int l, int m, int r) 
{ 
    int i, j, k; 
    int n1 = m - l + 1; 
    int n2 =  r - m; 
  
    /* create temp arrays */
    lli L[n1], R[n2]; 
  
    /* Copy data to temp arrays L[] and R[] */
    for (i = 0; i < n1; i++) 
        L[i] = arr[l + i]; 
    for (j = 0; j < n2; j++) 
        R[j] = arr[m + 1+ j]; 
  
    /* Merge the temp arrays back into arr[l..r]*/
    i = 0; // Initial index of first subarray 
    j = 0; // Initial index of second subarray 
    k = l; // Initial index of merged subarray 
    while (i < n1 && j < n2) 
    { 
        if (L[i] <= R[j]) 
        { 
            arr[k] = L[i]; 
            i++; 
        } 
        else
        { 
            arr[k] = R[j]; 
            j++; 
        } 
        k++; 
    } 
  
    /* Copy the remaining elements of L[], if there 
       are any */
    while (i < n1) 
    { 
        arr[k] = L[i]; 
        i++; 
        k++; 
    } 
  
    /* Copy the remaining elements of R[], if there 
       are any */
    while (j < n2) 
    { 
        arr[k] = R[j]; 
        j++; 
        k++; 
    } 
} 
  
/* l is for left index and r is right index of the 
   sub-array of arr to be sorted */
void mergeSort(lli arr[], int l, int r) 
{ 
    if (l < r) 
    { 
        // Same as (l+r)/2, but avoids overflow for 
        // large l and h 
        int m = l+(r-l)/2; 
  
        // Sort first and second halves 
        mergeSort(arr, l, m); 
        mergeSort(arr, m+1, r); 
  
        merge(arr, l, m, r); 
    } 
} 


